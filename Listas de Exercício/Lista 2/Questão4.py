valor1 = int(input('Digite um valor: '))
valor2 = int(input('Digite um valor: '))

if valor1 > valor2:
    print(f"O primeiro valor de {valor1} é maior que o segundo valor de {valor2}")
elif valor1 < valor2:
    print(f"O primeiro valor de {valor1} é menor que o segundo valor de {valor2}")
else:
    print(f"Os dois valores são iguais")
