Candidato = int(input("Digite o número do seu candidato para votar: "))
print(Candidato)

if Candidato == 1:
    print('Você votou no candidato A!')
elif Candidato == 2:
    print('Você votou no candidato B!')
elif Candidato == 3:
    print('Você votou no candidato C!')
else:
    print('Você votou nulo!')

